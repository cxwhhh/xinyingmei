-- 创建新闻资讯表
CREATE TABLE IF NOT EXISTS `news` (
    `id` bigint(20) NOT NULL AUTO_INCREMENT COMMENT '新闻ID',
    `title` varchar(255) NOT NULL COMMENT '新闻标题',
    `summary` varchar(800) DEFAULT NULL COMMENT '新闻摘要',
    `content` longtext DEFAULT NULL COMMENT '新闻内容',
    `image_url` varchar(500) DEFAULT NULL COMMENT '新闻配图URL',
    `category` varchar(50) DEFAULT NULL COMMENT '新闻分类',
    `views` int(11) NOT NULL DEFAULT 0 COMMENT '浏览次数',
    `create_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    PRIMARY KEY (`id`),
    KEY `idx_news_category` (`category`),
    KEY `idx_news_create_time` (`create_time`)
) ENGINE = InnoDB DEFAULT CHARSET = utf8mb4 COLLATE = utf8mb4_unicode_ci COMMENT = '新闻资讯表';

